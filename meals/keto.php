                <h2>Cooking Instructions</h2>
            </div>
            <div class="exercise-description">
                <!-- <p><b>Cook Time:</b> 15 Minutes</p> -->
               <p> <b>Cooking Instructions</b> </p>
                <ol>
                <li>Prep all ingredients so that they are ready to use.</li>
                    <li>Marinate chicken in garlic, ginger, soy sauce, and vinegar.</li>
                    <li>Add half of the avocado oil in a pan (or wok) over high heat. Once hot, add the onion and cook for 1-2 minutes or until soft, then add the red bell pepper and broccoli.</li>
                    <li>Once broccoli is cooked, but still crisp, remove the vegetables from the pan and place in a bowl. Set aside.</li>
                    <li>Drain the chicken from the marinade (keeping the marinade to add later). Add the remaining avocado oil to your pan (or wok) and once hot, add the chicken. Cook the chicken over high heat until completely cooked through, about 4-6 minutes.</li>
                    <li>Once the chicken is cooked, add all remaining ingredients into the pan (including the pre-cooked vegetables and marinade).</li>
                    <li>Cook for 3-4 minutes more, until the mushrooms are al dente(still firm when bitten) and sauce has reduced. Serve hot and enjoy!</li>
                </ol>
