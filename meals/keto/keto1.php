                <p>Stir fry recipes usually contain three key components: veggies, protein, and sauce. To keep it keto, we will be using low-carb vegetables and sugar-free, starch-free sauce.</p>
                <b>Ingredients:</b>
                <ol>
                    <li>1.5 pound bone-in, skin-on chicken thighs, de-boned and cut into slices</li>
                    <li>2 whole garlic cloves, minced</li>
                    <li>1 tablespoon ginger, peeled and grated</li>
                    <li>1/4 cup soy sauce, use tamari or coconut aminos if avoiding soy</li>
                    <li>1 tablespoon apple cider vinegar</li>
                    <li>3 tablespoon avocado oil</li>
                    <li>1 medium onion, chopped</li>
                    <li>7 ounce broccoli</li>
                    <li>1/2 medium red bell pepper, sliced</li>
                    <li>3 ounce cremini mushrooms, sliced</li>
                    <li>2 teaspoon sesame oil</li>
                    <li>1/2 teaspoon red pepper flakes</li>
                </ol>
                <p></p>
