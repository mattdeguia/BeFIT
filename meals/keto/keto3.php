                <p><b>Cook Time:</b> 10 Minutes</p>
               <p> <b>Cooking Instructions</b> </p>
                <ol>
                    <li>Gather all ingredients and prep as needed.</li>
                    <li>Cut the salmon into small cubes and place into a bowl.</li>
                    <li>Add the sesame oil, soy sauce, and salt to the salmon and mix together. Set aside.</li>
                    <li>In a bowl, place the cabbage, cucumber, and avocado to act as a bed for the salmon.</li>
                    <li>In a separate container, mix together the mayonnaise, soy sauce, and liquid stevia.</li>
                    <li>Place the marinated salmon over the vegetables, then garnish with cilantro. Drizzle the mayonnaise mixture
                        over the top and optionally garnish with sesame seeds as well. <b>Enjoy immediately after serving!</li>
                </ol>
