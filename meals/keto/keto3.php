                <p>Poke is one of the main dishes of Native Hawaiian cuisine, which usually consists of raw fish which is marinated in soy sauce and other seasonings. If you don’t like seafood, use a fully-cooked chicken thigh or breast instead. As with any other changes you make to the recipe, don’t forget to consider the changes it makes to the fat, carb, protein, and calorie content of your keto poke bowl.</p>
                <b>Ingredients:</b>
                <ol>
                    <li>8 oz fresh salmon, skinless</li>
                    <li>1 tbsp seasame oil</li>
                    <li>1 tsp soy sause (you can also use tamari, a substitute with less salt)</li>
                    <li>Salt, to taste </li>
                    <li>6 oz cabbage, thinly sliced</li>
                    <li>4 oz cucumber, sliced</li>
                    <li>1/2 medium avocado, sliced</li>
                    <li>1/4 cup cilantro</li>
                    <li>2 tsp mayonnaise</li>
                    <li>2 drops of liquid stevia</li>
                </ol>
                <p></p>
