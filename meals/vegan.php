                <p>Shrimp Scampi is a very simplistic dish that takes little to no preparation time.</p>
                <b>Ingredients:</b>
                <ol>
                    <li>2 tsp kosher salt (+ more to taste)</li>
                    <li>1/2 lb linguine pasta</li>
                    <li>3 Tbsp unsalted butter</li>
                    <li>2 Tbsp olive oil</li>
                    <li>3 garlic cloves, crushed</li>
                    <li>1/4 tsp crushed red pepper flake</li>
                    <li>1/2 cup dry white win</li>
                    <li>1 lb frozen shrimp, defrosted and peeled</li>
                    <li>1 tsp freshly ground black pepper</li>
                    <li>Juice of 1 large lemon</li>
                    <li>2 Tbsp parsley, chopped </li>
                </ol>
                <p></p>
            </div>
