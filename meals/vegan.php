                <h2>Ingredients and Cooking Instructions</h2>
            </div>
            <div class="exercise-description">
                <p><b>Cook Time:</b> 15 Minutes</p>
               <p> <b>Cooking Instructions</b> </p>
                <ol>
                    <li> Fill a large pot 2/3 of the way full with water and add 2 teaspoons of salt. Bring to a boil.</li>
                    <li>Meanwhile, heat the butter and olive oil over medium-high heat in a large skillet.</li>
                    <li>Once the butter is fully melted, add the crushed garlic and red pepper flakes. Cook until fragrant, about 30 seconds.</li>
                    <li>Add the pasta to the boiling water and cook until al dente according to the instructions on the box.</li>
                    <li>Add the wine to the skillet and cook until reduced by half, about 3-5 minutes.</li>
                    <li>Add the shrimp and cook on each side for about 45 seconds to 1 minute.</li>
                    <li>Add the lemon juice and stir to incorporate.</li>
                    <li>Drain the pasta, but reserve 1/4 cup of the water</li>
                    <li>Add the pasta to the skillet and stir to combine. If the sauce looks separated, add the reserved pasta water.</li>
                    <li>Season to taste and garnish with parsley.</li>
                </ol>
