          <p>Farmer's Walk on Toes is a functional exercise that strengthens the calves, improves balance, and engages the core muscles.</p>
          <ol>
            <li>Stand with your feet hip-width apart, holding a dumbbell in each hand at your sides.</li>
            <li>Raise your heels off the ground, balancing on the balls of your feet.</li>
            <li>Begin walking forward, maintaining your balance on your toes and keeping your core engaged.</li>
            <li>Walk for the desired distance or time, then lower your heels and rest.</li>
          </ol>
          <p>Reps: 3 sets of 30-60 seconds or a set distance, with a 60-second rest between sets.</p>
