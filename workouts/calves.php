        <div class="exercise-description">
          <p>Standing Calf Raises target the gastrocnemius muscle in the calves and can be performed with or without added weight.</p>
          <ol>
            <li>Stand with your feet hip-width apart, either holding dumbbells by your sides or using no weight.</li>
            <li>Slowly raise your heels off the ground, pressing through the balls of your feet and engaging your calves.</li>
            <li>Hold the contraction at the top of the movement for a second, then slowly lower your heels back to the ground.</li>
            <li>Repeat for the desired number of repetitions.</li>
          </ol>
          <p>Reps: 3 sets of 12-15 reps, with a 60-second rest between sets.</p>
        </div>
