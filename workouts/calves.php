        <div class="exercise-description">
          <p>Seated Calf Raises isolate and target the soleus muscle in the calves, helping to improve lower leg strength and shape.</p>
          <ol>
            <li>Sit on a seated calf raise machine with your thighs under the pads and your feet flat on the platform.</li>
            <li>Lower your heels as far as possible to stretch your calves.</li>
            <li>Push through the balls of your feet to raise your heels as high as possible, contracting your calves at the top of the movement.</li>
            <li>Slowly lower your heels back to the starting position and repeat for the desired number of repetitions.</li>
          </ol>
          <p>Reps: 3 sets of 12-15 reps, with a 60-second rest between sets.</p>
        </div>
      </div>
