          <p>Lunges are an excellent exercise for strengthening the glutes, quads, and hamstrings.</p>
          <ol>
            <li>Stand straight with your feet hip-width apart.</li>
            <li>Take a step forward with your right foot and lower your body until your right knee is at a 90-degree angle.</li>
            <li>Push up through your right foot to return to the starting position.</li>
            <li>Repeat with the left foot.</li>
          </ol>
          <p>Reps: 3 sets of 10-15 reps per leg, with a 60-second rest between sets.</p>
        </div>
