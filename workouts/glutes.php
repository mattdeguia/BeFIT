          <ol>
            <li>Stand with your feet hip-width apart, and hold a barbell or two dumbbells at hip level.</li>
            <li>Lower the weights to your shins by pushing your hips back and bending your knees.</li>
            <li>Return to the starting position by pushing your hips forward and standing tall.</li>
          </ol>
          <p>Reps: 3 sets of 10-15 reps, with a 60-second rest between sets.</p>
