          <ol>
            <li>Start on all fours with your hands directly under your shoulders and your knees directly under your hips.</li>
            <li>Keeping your right knee at a 90-degree angle, lift your right leg until your thigh is parallel to the floor.</li>
            <li>Lower your leg back to the starting position.</li>
            <li>Repeat with the left leg.</li>
          </ol>
          <p>Reps: 3 sets of 10-15 reps per leg, with a 60-second rest between sets.</p>
